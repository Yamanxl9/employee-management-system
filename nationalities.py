def get_nationality_name(code, language='en'):
    """
    الحصول على اسم الجنسية بالكامل
    
    Args:
        code: كود الجنسية (مثل 'SY', 'IN')
        language: اللغة ('en' للإنجليزية، 'ar' للعربية)
    
    Returns:
        اسم الجنسية بالكامل أو الكود إذا لم يتم العثور عليه
    """
    if code in NATIONALITIES:
        return NATIONALITIES[code].get(language, code)
    return code

def get_all_nationalities():
    """
    الحصول على جميع الجنسيات
    
    Returns:
        قائمة بجميع الجنسيات مع أكوادها وأسمائها
    """
    result = []
    for code, names in NATIONALITIES.items():
        result.append({
            'code': code,
            'name_en': names['en'],
            'name_ar': names['ar']
        })
    return sorted(result, key=lambda x: x['name_en'])
